// Array methods

let MyArr = [20, 54, 63, 78];
console.log(MyArr);

// push, pop, shift, unshift
MyArr.push(100); // sonyna element kosu
console.log(MyArr);

MyArr.pop(); // sonynan element oshiru
console.log(MyArr);

MyArr.shift(); // basynan element oshiru
console.log(MyArr);

MyArr.unshift(200); // basyna element kosu
console.log(MyArr);